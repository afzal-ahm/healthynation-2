             <p style="text-align:justify;">A Viral Flu Panel (Influenza A&B, H3N2, H1N1, RSV) Qualitative RT-PCR is tailored to detect and identify the causative agents of respiratory infections—Influenza A, Influenza B, and Respiratory Syncytial Virus (RSV). Available at an affordable price in Gurgaon with Tata 1mg labs, this panel helps in the thorough assessment of different strains of flu viruses causing potential respiratory illness, particularly during flu season.</p>
             <p style="text-align:justify;">Your doctor may suggest a viral flu panel when you have symptoms such as cough, sore throat, runny nose, nasal congestion, headache, and low-grade fever indicative of flu or a similar respiratory illness. A duly filled Swine Flu/H1N1 Form is needed to be filled before sample collection. This form can be provided by the laboratory personnel (phlebotomist) before sample withdrawal. </p>
             <span class="fs-5">What does Viral Flu Panel (Influenza A&B, H3N2, H1N1, RSV) Qualitative RT-PCR</span>
             <p class="fs-6">Contains 6 tests</p>
             <hr>
             <p style="text-align: justify;">A viral flu panel tests for influenza A virus along with its subtypes H3N2 and H1N1 (swine flu), Influenza B virus, and respiratory syncytial virus (RSV). Viral flu spreads mainly through tiny droplets when an individual with flu coughs, sneezes, or talks. These droplets are dispersed into the air and can infect individuals in close proximity. Also, the virus can spread by touching surfaces that may have influenza viruses on them and then touching your nose, mouth, or eyes. This panel helps differentiate between different strains of influenza viruses and identifies RSV infection. A Viral Flu Panel (Influenza A&B, H3N2, H1N1, RSV) Qualitative RT-PCR can be helpful during the flu season when there's a surge in various respiratory illnesses that show symptoms similar to the flu.</p>
