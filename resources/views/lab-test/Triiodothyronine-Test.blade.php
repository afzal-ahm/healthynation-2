             </div>  
             <span class="fs-5 mb-2">What is T3 Triiodothyronine Total?</span> 
             <p style="text-align:justify;">The Triiodothyronine Total test measures the level of total triiodothyronine hormone in the blood. This test helps assess overall thyroid function and detect possible thyroid disorders. It is also performed in combination with total T4 and TSH tests as part of a thyroid profile total test.</p>
             <p class="mb-2" style="text-align: justify;">Triiodothyronine, also known as T3, is a hormone produced by the thyroid gland. The thyroid is a small, butterfly-shaped gland located at the front of your neck. It helps regulate the body’s metabolism and growth, heart rate, temperature, and the development of tissues. T3 along with thyroxine (T4), another hormone produced by the thyroid gland, plays an important role in the growth and development of the nervous system and the brain. T3 is the active form of thyroid hormone that exists in the blood in two forms: free T3 (the form that is not bound to proteins) and bound T3 (the form attached to proteins). More than 99% of the T3 is in the bound form; only a small proportion is free and not attached to proteins.</p>
             <p class="mb-2" style="text-align:justify;">The Triiodothyronine Total test measures the total level (both free and bound forms) of the T3 hormone in the blood. Abnormal levels of T3 hormone can result in either hyperthyroidism (the thyroid gland produces too much T3) or hypothyroidism (the gland produces less T3). Your doctor may suggest this test if you show symptoms of hyperthyroidism, like increased appetite, weight loss, sweating, anxiety, tremors, etc., or hypothyroidism, like fatigue, weight gain, dry skin, constipation, etc.</p>
             <p class="mb-2" style="text-align: justify;">No special preparations are needed for the Triiodothyronine Total test. However, let your doctor know about all the medications you take as some of them can affect thyroid function. Biotin (Vitamin B7) is a commonly taken over-the-counter supplement that can lead to some aberrancy in your thyroid test results even if they are normal. Therefore, avoid taking biotin 2 days before this test.</p>
             <p class="mb-2" style="text-align: justify;">Test result ranges are approximate and may differ slightly between different labs depending on the methodology and laboratory guidelines. Talk to your doctor about your specific test results. The results will help them evaluate your medical condition, make recommendations for lifestyle modifications such as diet and exercise, assess whether or not medication will be required to manage your condition, and formulate your overall treatment plan.</p>
             <span class="fs-5">What is T3 Triiodothyronine Total used for?</span>
             <ul>
                <p>The T3 Triiodothyronine Total test is done:</p>
                <li class="mb-2" style="font-size:14px;">To measure the levels of total T3 hormone in the body.</li>
                <li class="mb-2" style="font-size:14px;">As part of a thyroid profile-total along with total T4 and TSH tests.</li>
                <li class="mb-2" style="font-size:14px;">To evaluate symptoms suggestive of an imbalance in thyroid hormone levels, such as fatigue, weight loss, hair loss, and changes in heart rate.</li>
                <li class="mb-2" style="font-size:14px;">To diagnose and monitor the treatment of hypothyroidism or hyperthyroidism.</li>
                <li class="mb-2" style="font-size:14px;">To detect an underactive thyroid gland in newborn babies.</li>
                <li class="mb-2" style="font-size:14px;">To evaluate disorders such as thyroid nodules, goiters, and pituitary or hypothalamus gland disorders.</li>
             </ul>
             <span class="fs-5">What does Triiodothyronine Total measure?</span>
             <hr>
             <p style="text-align: justify;">The Triiodothyronine Total test measures triiodothyronine, also known as T3, hormone that is produced by the thyroid gland. T3 hormone plays an important role in regulating the body's metabolism, energy levels, and growth & development. It exists in the blood in two forms: free T3 and bound T3. Free T3 is not bound to proteins in the blood and is the active form of T3. Whereas, bound T3 is bound to proteins, such as albumin and thyroid hormone binding globulin (THBG), which prevent it from entering the body tissues.</p>

