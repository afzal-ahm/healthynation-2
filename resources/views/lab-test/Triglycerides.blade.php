             <span class="fs-5 mb-2">What is Triglycerides?</span> 
             <p style="text-align:justify;">The Triglycerides test is a blood test that measures the levels of triglycerides in the body. This test is used to evaluate your risk of developing cardiovascular diseases, especially when you have risk factors, such as obesity, diabetes, and high blood pressure. It is often done as part of a routine health checkup or a lipid profile.</p>
             <p class="mb-2" style="text-align:justify;">Triglycerides are a type of fat found in your blood that the body uses for energy. They are stored in fat cells and released when your body needs extra fuel. A Triglycerides test is often performed along with low-density lipoprotein (LDL) cholesterol and high-density lipoprotein (HDL) cholesterol tests. This test is vital for managing your overall heart health as high levels of triglycerides can contribute to the hardening and narrowing of your arteries.</p>
             <p class="mb-2" style="text-align: justify;">Your doctor may suggest the Triglycerides test if you have risk factors that may contribute to high triglyceride levels, such as obesity, a high-carbohydrate diet, poorly-controlled diabetes, etc. Abnormal triglyceride levels may not show any symptoms initially. However, if left untreated, high triglyceride levels (hypertriglyceridemia) can lead to several health complications, such as heart attack, stroke, and other cardiovascular conditions. On the other hand, low triglyceride levels (hypotriglyceridemia) are rare and can be caused by malnutrition or specific genetic conditions. Overnight fasting (8-12 hours) is recommended before undergoing the Triglycerides test. Do not eat or drink anything other than water during the fasting period. </p>
             <p class="mb-2" style="text-align: justify;">Test result ranges are approximate and may differ slightly between labs depending on the methodology and laboratory guidelines. Talk to your doctor about your specific test results. The results will help them determine your medical condition, make recommendations for lifestyle modifications such as diet and exercise, decide whether or not medication will be required to manage your condition, and formulate your overall treatment plan.</p>
