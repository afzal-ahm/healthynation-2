             <span class="fs-5 mb-2">What is Cholesterol - Total?</span> 
             <p style="text-align:justify;">The Cholesterol - Total test measures the total amount of cholesterol (fats) in the blood. This test helps analyze your overall heart health and monitor the effectiveness of any ongoing cholesterol-lowering treatment. It is a single test, often done as part of the lipid profile test.</p>
             <p class="mb-2" style="text-align:justify;">Cholesterol is a waxy, fat-like substance found in all body cells. An optimal amount of cholesterol is essential for various bodily functions. However, too much cholesterol can build up in the blood vessels and increase the risk of heart disease, stroke, or other health problems.</p>
             <p class="mb-2" style="text-align: justify;">The Cholesterol - Total test measures the total amount of cholesterol in the blood, which is a combination of low-density lipoprotein (LDL) cholesterol, high-density lipoprotein (HDL) cholesterol, and very-low-density lipoprotein (VLDL) cholesterol. It is usually recommended for individuals with risk factors for heart disease, such as high blood pressure, diabetes, obesity, a family history of heart disease, or an unhealthy lifestyle. However, the total cholesterol test may also be done routinely for cardiovascular risk assessments and in individuals with symptoms or conditions suggestive of high cholesterol levels.</p>
             <p class="mb-2" style="text-align: justify;">The Cholesterol - Total test helps detect conditions like high cholesterol (hyperlipidemia) or low cholesterol (hypolipidemia). Hyperlipidemia is a condition in which your blood contains excessive amounts of lipids and increases your risk of heart disease and stroke. The signs and symptoms may not be evident until a significant blockage or a heart event occurs. Hypolipidemia, while less common, is a condition where your blood does not contain enough lipids, potentially leading to issues with nerve function and an increased risk of infections. </p>
             <p class="mb-2" style="text-align: justify;">No fasting is required for this test. You can eat and drink normally as per your routine. Also, your doctor may suggest you stop taking certain medications, such as statins, beta-blockers, etc., a few days before this test, as these medicines could affect your test results.</p>
             <p class="mb-2" style="text-align: justify;">Test result ranges are approximate and may differ slightly between labs depending on the methodology and laboratory guidelines. Talk to your doctor about your specific test results. The results will help them evaluate your medical condition, make diet, exercise, or medication recommendations, and formulate an overall treatment plan.</p>
             <span class="fs-5">What is Cholesterol - Total used for?</span>
             <ul>
                <p>A Cholesterol - Total test is done:</p>
                <li class="mb-2" style="font-size:14px;">As part of routine health checkups to measure the blood cholesterol level.</li>
                <li class="mb-2" style="font-size:14px;">To determine an individual's risk of developing heart conditions, stroke, and peripheral artery disease.</li>
                <li class="mb-2" style="font-size:14px;">To monitor the effectiveness of any ongoing cholesterol-lowering treatment.</li>
                <li class="mb-2" style="font-size:14px;">To monitor cholesterol levels in individuals with a family history of high cholesterol or premature heart disease.</li>
