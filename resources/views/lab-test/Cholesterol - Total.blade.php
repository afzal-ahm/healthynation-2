             <span class="fs-5">What does Cholesterol - Total measure?</span>
             <hr>
             <p style="text-align: justify;">The Cholesterol - Total test measures the total amount of cholesterol (fats) in your blood. Cholesterol is mainly synthesized in the liver and partially in the intestines. It acts as a building block for cell membranes, is a precursor to vital hormones, and helps produce bile acids that help digest fats. Cholesterol is transported through the blood as lipoproteins: low-density lipoprotein (LDL) and high-density lipoprotein (HDL). An optimal amount of these proteins is necessary for proper body functioning.</p>
